package homework.day1.pretask;

public class IncrementDecrementExample {
    public static void main(String[] args) {
        int incrementDecrementVar = 10;
        System.out.println("Initial value: " + incrementDecrementVar);
        incrementDecrementVar++;
        System.out.println("After increment: " + incrementDecrementVar);
        incrementDecrementVar--;
        System.out.println("After decrement: " + incrementDecrementVar);
    }
}